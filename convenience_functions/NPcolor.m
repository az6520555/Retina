          6,14,22,30,38,46,54,62,...
          7,15,23,31,39,47,55,63,...
            16,24,32,40,48,56];

    load(file)
    for n=1:length(P_channel)
            subplot(8,8,rr(P_channel(n)))
            set(gca,'Color',[0.8 1 0.8])
    end
